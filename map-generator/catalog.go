// SpectralType represents a star's spectral classification.
type SpectralType int

const (
	SpectralO SpectralType = iota
	SpectralB
	SpectralA
	SpectralF
	SpectralG
	SpectralK
	SpectralM
)

func (s SpectralType) String() string {
	return [...]string{"O", "B", "A", "F", "G", "K", "M"}[s]
}

